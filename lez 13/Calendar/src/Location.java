
public class Location {
	String city;
	String address;
	public Location(String city, String address) {
		super();
		this.city = city;
		this.address = address;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}
